            attachment_url VARCHAR2(255 CHAR),
            created_at TIMESTAMP(6) WITH TIME ZONE NOT NULL,
            edited_at TIMESTAMP(6) WITH TIME ZONE,
            CONSTRAINT pk_messages PRIMARY KEY (id)
        )
    ]');

    create_table_if_missing('conversation_participants', q'[
        CREATE TABLE conversation_participants (
            conversation_id VARCHAR2(255 CHAR) NOT NULL,
            user_id VARCHAR2(255 CHAR) NOT NULL,
            last_read_at TIMESTAMP(6) WITH TIME ZONE,
