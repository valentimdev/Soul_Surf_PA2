            longitude FLOAT(53),
            CONSTRAINT pk_beaches PRIMARY KEY (id)
        )
    ]');

    create_table_if_missing('conversations', q'[
        CREATE TABLE conversations (
            id VARCHAR2(36 CHAR) NOT NULL,
            created_at TIMESTAMP(6) WITH TIME ZONE NOT NULL,
            is_group NUMBER(1,0) DEFAULT 0 NOT NULL,
            CONSTRAINT pk_conversations PRIMARY KEY (id)
        )
    ]');

    create_table_if_missing('points_of_interest', q'[
        CREATE TABLE points_of_interest (
            id NUMBER(19,0) GENERATED BY DEFAULT ON NULL AS IDENTITY,
            nome VARCHAR2(255 CHAR) NOT NULL,
            descricao CLOB,
            categoria VARCHAR2(255 CHAR) NOT NULL,
            latitude FLOAT(53) NOT NULL,
            longitude FLOAT(53) NOT NULL,
            telefone VARCHAR2(255 CHAR),
