DECLARE
    v_count NUMBER;

    PROCEDURE create_table_if_missing(p_table_name IN VARCHAR2, p_ddl IN CLOB) IS
    BEGIN
        SELECT COUNT(*)
        INTO v_count
        FROM user_tables
        WHERE table_name = UPPER(p_table_name);

        IF v_count = 0 THEN
            EXECUTE IMMEDIATE p_ddl;
        END IF;
    END;

    PROCEDURE add_column_if_missing(p_table_name IN VARCHAR2, p_column_name IN VARCHAR2, p_definition IN VARCHAR2) IS
    BEGIN
        SELECT COUNT(*)
        INTO v_count
        FROM user_tab_columns
        WHERE table_name = UPPER(p_table_name)
          AND column_name = UPPER(p_column_name);

        IF v_count = 0 THEN
            EXECUTE IMMEDIATE 'ALTER TABLE ' || p_table_name || ' ADD (' || p_definition || ')';
        END IF;
    END;

    PROCEDURE add_index_if_missing(p_index_name IN VARCHAR2, p_ddl IN VARCHAR2) IS
    BEGIN
        SELECT COUNT(*)
        INTO v_count
        FROM user_indexes
        WHERE index_name = UPPER(p_index_name);

        IF v_count = 0 THEN
            EXECUTE IMMEDIATE p_ddl;
        END IF;
    END;

    PROCEDURE drop_constraint_if_exists(p_table_name IN VARCHAR2, p_constraint_name IN VARCHAR2) IS
    BEGIN
        SELECT COUNT(*)
        INTO v_count
        FROM user_constraints
        WHERE table_name = UPPER(p_table_name)
          AND constraint_name = UPPER(p_constraint_name);

        IF v_count > 0 THEN
            EXECUTE IMMEDIATE 'ALTER TABLE ' || p_table_name || ' DROP CONSTRAINT ' || p_constraint_name;
        END IF;
    END;

    PROCEDURE add_constraint_if_missing(p_table_name IN VARCHAR2, p_constraint_name IN VARCHAR2, p_definition IN VARCHAR2) IS
    BEGIN
        SELECT COUNT(*)
        INTO v_count
        FROM user_constraints
        WHERE table_name = UPPER(p_table_name)
          AND constraint_name = UPPER(p_constraint_name);

        IF v_count = 0 THEN
            EXECUTE IMMEDIATE 'ALTER TABLE ' || p_table_name || ' ADD CONSTRAINT ' || p_constraint_name || ' ' || p_definition;
        END IF;
    END;

    PROCEDURE normalize_boolean_column(p_table_name IN VARCHAR2, p_column_name IN VARCHAR2, p_default IN NUMBER) IS
    BEGIN
        EXECUTE IMMEDIATE 'UPDATE ' || p_table_name || ' SET ' || p_column_name || ' = ' || p_default ||
                          ' WHERE ' || p_column_name || ' IS NULL';
        EXECUTE IMMEDIATE 'ALTER TABLE ' || p_table_name || ' MODIFY (' || p_column_name ||
                          ' DEFAULT ' || p_default || ' NOT NULL)';
    EXCEPTION
        WHEN OTHERS THEN
            IF SQLCODE NOT IN (-904, -1442) THEN
                RAISE;
            END IF;
    END;

    PROCEDURE replace_column_check(
        p_table_name IN VARCHAR2,
        p_column_name IN VARCHAR2,
        p_constraint_name IN VARCHAR2,
        p_definition IN VARCHAR2
    ) IS
    BEGIN
        FOR c IN (
            SELECT uc.constraint_name
            FROM user_constraints uc
            JOIN user_cons_columns ucc ON uc.constraint_name = ucc.constraint_name
            WHERE uc.table_name = UPPER(p_table_name)
              AND ucc.table_name = UPPER(p_table_name)
              AND ucc.column_name = UPPER(p_column_name)
              AND uc.constraint_type = 'C'
              AND NVL(UPPER(uc.search_condition_vc), ' ') NOT LIKE '%' || UPPER(p_column_name) || ' IS NOT NULL%'
        ) LOOP
            EXECUTE IMMEDIATE 'ALTER TABLE ' || p_table_name || ' DROP CONSTRAINT ' || c.constraint_name;
        END LOOP;

        add_constraint_if_missing(p_table_name, p_constraint_name, p_definition);
    END;
BEGIN
    create_table_if_missing('users', q'[
        CREATE TABLE users (
            id NUMBER(19,0) GENERATED BY DEFAULT ON NULL AS IDENTITY,
            email VARCHAR2(255 CHAR),
            password VARCHAR2(255 CHAR),
            username VARCHAR2(255 CHAR) NOT NULL,
            foto_capa VARCHAR2(255 CHAR),
            foto_perfil VARCHAR2(255 CHAR),
            bio VARCHAR2(255 CHAR),
            admin NUMBER(1,0) DEFAULT 0 NOT NULL,
            banned NUMBER(1,0) DEFAULT 0 NOT NULL,
            CONSTRAINT pk_users PRIMARY KEY (id),
            CONSTRAINT uk_users_email UNIQUE (email),
            CONSTRAINT uk_users_username UNIQUE (username)
        )
    ]');

    create_table_if_missing('beaches', q'[
        CREATE TABLE beaches (
            id NUMBER(19,0) GENERATED BY DEFAULT ON NULL AS IDENTITY,
