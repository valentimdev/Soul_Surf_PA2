            CONSTRAINT pk_points_of_interest PRIMARY KEY (id)
        )
    ]');

    create_table_if_missing('admin_audit_logs', q'[
        CREATE TABLE admin_audit_logs (
            id NUMBER(19,0) GENERATED BY DEFAULT ON NULL AS IDENTITY,
            actor_email VARCHAR2(255 CHAR) NOT NULL,
            action VARCHAR2(255 CHAR) NOT NULL,
            target_type VARCHAR2(255 CHAR) NOT NULL,
            target_id NUMBER(19,0) NOT NULL,
            details VARCHAR2(255 CHAR),
            created_at TIMESTAMP(6) NOT NULL,
            CONSTRAINT pk_admin_audit_logs PRIMARY KEY (id)
        )
    ]');

    create_table_if_missing('password_reset_tokens', q'[
        CREATE TABLE password_reset_tokens (
            id NUMBER(19,0) GENERATED BY DEFAULT ON NULL AS IDENTITY,
            token VARCHAR2(255 CHAR) NOT NULL,
            user_id NUMBER(19,0) NOT NULL,
            expiry_date TIMESTAMP(6) WITH TIME ZONE NOT NULL,
            CONSTRAINT pk_password_reset_tokens PRIMARY KEY (id),
            CONSTRAINT uk_password_reset_tokens_token UNIQUE (token),
            CONSTRAINT fk_password_reset_tokens_user FOREIGN KEY (user_id) REFERENCES users (id)
        )
    ]');

    create_table_if_missing('posts', q'[
        CREATE TABLE posts (
            id NUMBER(19,0) GENERATED BY DEFAULT ON NULL AS IDENTITY,
            descricao VARCHAR2(255 CHAR) NOT NULL,
            publico NUMBER(1,0) DEFAULT 1 NOT NULL,
            caminho_foto VARCHAR2(255 CHAR),
            data TIMESTAMP(6) NOT NULL,
            usuario_id NUMBER(19,0) NOT NULL,
            beach_id NUMBER(19,0),
            CONSTRAINT pk_posts PRIMARY KEY (id),
            CONSTRAINT fk_posts_usuario FOREIGN KEY (usuario_id) REFERENCES users (id),
            CONSTRAINT fk_posts_beach FOREIGN KEY (beach_id) REFERENCES beaches (id)
        )
    ]');

    create_table_if_missing('comments', q'[
        CREATE TABLE comments (
            id NUMBER(19,0) GENERATED BY DEFAULT ON NULL AS IDENTITY,
            texto VARCHAR2(255 CHAR) NOT NULL,
            data TIMESTAMP(6) NOT NULL,
            usuario_id NUMBER(19,0) NOT NULL,
            post_id NUMBER(19,0) NOT NULL,
            parent_id NUMBER(19,0),
            CONSTRAINT pk_comments PRIMARY KEY (id),
            CONSTRAINT fk_comments_usuario FOREIGN KEY (usuario_id) REFERENCES users (id),
            CONSTRAINT fk_comments_post FOREIGN KEY (post_id) REFERENCES posts (id),
            CONSTRAINT fk_comments_parent FOREIGN KEY (parent_id) REFERENCES comments (id)
        )
    ]');

    create_table_if_missing('likes', q'[
        CREATE TABLE likes (
            id NUMBER(19,0) GENERATED BY DEFAULT ON NULL AS IDENTITY,
            post_id NUMBER(19,0) NOT NULL,
            usuario_id NUMBER(19,0) NOT NULL,
            data TIMESTAMP(6) NOT NULL,
            CONSTRAINT pk_likes PRIMARY KEY (id),
            CONSTRAINT uk_likes_post_usuario UNIQUE (post_id, usuario_id),
            CONSTRAINT fk_likes_post FOREIGN KEY (post_id) REFERENCES posts (id),
            CONSTRAINT fk_likes_usuario FOREIGN KEY (usuario_id) REFERENCES users (id)
        )
    ]');

    create_table_if_missing('mensagens', q'[
        CREATE TABLE mensagens (
            id NUMBER(19,0) GENERATED BY DEFAULT ON NULL AS IDENTITY,
            texto CLOB NOT NULL,
            data TIMESTAMP(6) NOT NULL,
            autor_id NUMBER(19,0) NOT NULL,
            praia_id NUMBER(19,0) NOT NULL,
            CONSTRAINT pk_mensagens PRIMARY KEY (id),
            CONSTRAINT fk_mensagens_autor FOREIGN KEY (autor_id) REFERENCES users (id),
            CONSTRAINT fk_mensagens_praia FOREIGN KEY (praia_id) REFERENCES beaches (id)
        )
    ]');

    create_table_if_missing('notifications', q'[
        CREATE TABLE notifications (
            id NUMBER(19,0) GENERATED BY DEFAULT ON NULL AS IDENTITY,
            recipient_id NUMBER(19,0) NOT NULL,
            sender_id NUMBER(19,0) NOT NULL,
            type VARCHAR2(255 CHAR) NOT NULL,
            post_id NUMBER(19,0),
            comment_id NUMBER(19,0),
            is_read NUMBER(1,0) DEFAULT 0 NOT NULL,
            read NUMBER(1,0) DEFAULT 0 NOT NULL,
            created_at TIMESTAMP(6) NOT NULL,
            CONSTRAINT pk_notifications PRIMARY KEY (id),
            CONSTRAINT fk_notifications_recipient FOREIGN KEY (recipient_id) REFERENCES users (id),
            CONSTRAINT fk_notifications_sender FOREIGN KEY (sender_id) REFERENCES users (id),
            CONSTRAINT fk_notifications_post FOREIGN KEY (post_id) REFERENCES posts (id),
            CONSTRAINT fk_notifications_comment FOREIGN KEY (comment_id) REFERENCES comments (id)
        )
    ]');

    create_table_if_missing('push_tokens', q'[
        CREATE TABLE push_tokens (
            id NUMBER(19,0) GENERATED BY DEFAULT ON NULL AS IDENTITY,
            user_id NUMBER(19,0) NOT NULL,
            push_token VARCHAR2(512 CHAR) NOT NULL,
            platform VARCHAR2(32 CHAR) NOT NULL,
            is_active NUMBER(1,0) DEFAULT 1 NOT NULL,
            created_at TIMESTAMP(6) NOT NULL,
            updated_at TIMESTAMP(6) NOT NULL,
            last_used_at TIMESTAMP(6),
            CONSTRAINT pk_push_tokens PRIMARY KEY (id),
            CONSTRAINT uk_push_tokens_token UNIQUE (push_token),
            CONSTRAINT fk_push_tokens_user FOREIGN KEY (user_id) REFERENCES users (id)
        )
    ]');

    create_table_if_missing('messages', q'[
        CREATE TABLE messages (
            id VARCHAR2(36 CHAR) NOT NULL,
            conversation_id VARCHAR2(36 CHAR) NOT NULL,
            sender_id VARCHAR2(255 CHAR) NOT NULL,
