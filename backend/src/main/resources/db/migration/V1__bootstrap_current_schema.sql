    ]');

    add_column_if_missing('users', 'admin', 'admin NUMBER(1,0) DEFAULT 0 NOT NULL');
    add_column_if_missing('users', 'banned', 'banned NUMBER(1,0) DEFAULT 0 NOT NULL');
    add_column_if_missing('beaches', 'latitude', 'latitude FLOAT(53)');
    add_column_if_missing('beaches', 'longitude', 'longitude FLOAT(53)');
    add_column_if_missing('posts', 'publico', 'publico NUMBER(1,0) DEFAULT 1 NOT NULL');
    add_column_if_missing('conversations', 'is_group', 'is_group NUMBER(1,0) DEFAULT 0 NOT NULL');
    add_column_if_missing('notifications', 'is_read', 'is_read NUMBER(1,0) DEFAULT 0 NOT NULL');
    add_column_if_missing('notifications', 'read', 'read NUMBER(1,0) DEFAULT 0 NOT NULL');
    add_column_if_missing('push_tokens', 'is_active', 'is_active NUMBER(1,0) DEFAULT 1 NOT NULL');

    normalize_boolean_column('users', 'admin', 0);
    normalize_boolean_column('users', 'banned', 0);
    normalize_boolean_column('posts', 'publico', 1);
    normalize_boolean_column('notifications', 'is_read', 0);
    normalize_boolean_column('notifications', 'read', 0);
    normalize_boolean_column('push_tokens', 'is_active', 1);
    normalize_boolean_column('conversations', 'is_group', 0);

    replace_column_check(
        'notifications',
        'type',
        'chk_notifications_type',
        q'[CHECK (type IN ('MENTION','LIKE','COMMENT','REPLY','FOLLOW'))]'
    );

    replace_column_check(
        'points_of_interest',
        'categoria',
        'chk_points_of_interest_categoria',
        q'[CHECK (categoria IN ('SURF_SCHOOL','SWIMMING_SCHOOL','SURF_SHOP','BOARD_REPAIR','TOURIST_SPOT'))]'
    );

    add_index_if_missing('idx_push_tokens_user_id',
        'CREATE INDEX idx_push_tokens_user_id ON push_tokens (user_id)');
    add_index_if_missing('idx_push_tokens_user_active',
        'CREATE INDEX idx_push_tokens_user_active ON push_tokens (user_id, is_active)');
END;
